    * Ultra maratonda başarılı olabilmek için dayanıklılık ve kondisyondan sonraki en önemli unsur yarış öncesi ve süresince doğru beslenmektir. Yarış süresindeki beslenme dengeniz ve stratejiniz en az fiziksel dayanıklılığınız kadar önemlidir. <br />
