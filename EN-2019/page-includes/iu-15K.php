	-	The race requires high level physical and mental endurance. Thus the competitors must be well prepared. Each registrant signs a consent letter before the race.
<br />
	-	There will only be  general men and general women classifications in 15K category. No age classification will be made in IU-15K.<br /><br />
<strong>The classification for 30K / 60K / 100K categories will be as follows:</strong><br />

