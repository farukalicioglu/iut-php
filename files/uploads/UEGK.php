                        <h4 class="align-left"><strong>Öneriler & İpuçları</strong></h4>
                        <p>Etkinliğin gerçekleşeceği tarihteki hava ve coğrafi şartları göz önünde tutarak aşağıdaki malzemelerden uygun olanları yanınızda bulundurmanızda fayda vardır;</p>
