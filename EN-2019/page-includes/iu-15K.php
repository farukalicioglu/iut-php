	-	Professional rescue and health teams will be present during the race at all times. Main medical services will be given at the main camp and basic health services will be provided at the check points. The medical team will be on the course during the race. Medicals hold the right to disqualify a competitor in case there is a health issue. Yet it is preferred by the organizers that the competitor finishes the race.  <br />
	-	The race director is responsible of the race course and management during the race. The map of the race course will be delivered to competitors before the race. The race course is can be changed by the race director due to weather conditions or other reasons. Check point and main camp locations can again be changed by the race director. Race director briefs the competitors about the daily course each night.  <br />
	-	Competitors can commit their precious belongings and other belongings to staff before the race. These will be refunded at the end of the race. The responsibility of the equipment that will be carried in the backpacks during the race belongs to the competitor.  <br />
	-	Competitors may get sponsors. They are allowed to wear sponsor logo on their chests or backpacks as long as they don't cover the race number. Visibility of sponsor logos apart from these two locations is decided by the organization comitee. <br /> 
