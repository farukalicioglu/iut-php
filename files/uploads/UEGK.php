 kimi zaman şehir içinde kimi zaman ise ormanlık arazilerde gerçekleştirilir. Hangi şartta olursa olsun parkuru kafa lambası ile koşmak zorunludur. Etkinliğe gelmeden once kafa lambanızın sorunsuz bir şekilde çalıştığından ve yeterli görüşü sağladığından emin olun.</p>
