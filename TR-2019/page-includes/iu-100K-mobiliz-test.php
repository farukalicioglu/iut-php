    * Sırt çantasıyla yürümek/koşmak farklı kas gruplarına yük bindirir. Bu sebeple antrenmanların en az bir bölümünün sırt çantasıyla, tercihen yarışta kullanılacak sırt çantasıyla ve benzer ağırlıkta yapılması uygun olur. <br />
