                        <p>Yarış sonrası değiştirebilmek için katılımcıların yanlarında yedek kıyafet getirmesi önerilir. Bu malzemeler yarış esnasında organizasyon tarafından muhafaza edilecek ve finish noktasında teslim edilecektir.</p>
