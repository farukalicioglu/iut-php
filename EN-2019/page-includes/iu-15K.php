						<h4 class="align-left"><strong>RACE RULES</strong></h4>

						<p>
							-	Course must be completed in designated periods and conditions. Otherwise the race will not be coounted as completed.  <br />
	-	Competitors must be minimum 18 years old.  <br />
	-	The course comprises check points. Each competitor is obliged to reach passing through the check points. If the competitor fails to pass through any check point will be disqualified.<br />
	-	Each check point will be closed after a period which will be announced beforehand. Competitors who fail to reach the check point within this period will be disqualified. The timing of these periods is flexible due to race conditions and may be changed by the race director.  <br />
	-	Participants will receive water and food (snacks) support at the checkpoints.<br />
	-	All competitors are obliged to take and consume water. In case a competitor gets transfusion due to dehydration he/she gets time penalty of 2 hours for the first transfusion and 4 hours for the second. In case of a third transfusion, competitor is disqualified.  <br />
	-	Differing across categories, competitors are obliged to carry the respective mandatory equipment of their categories in their backpacks. Competitors who fail to present mandatory equipment during the race are subject to disqualification. Organization staff and the referees hold the right to equipment and backpack check anytime during the race.  <br />
	-	Using any means of transportation (motor vehicle, bicycle, horse, donkey, mule carriage etc.) regardless of duration and distance results in disqualification. <br />
	-	Competitors dropping litter anywhere out of main event area and specific points at check points are penalized with 2 hours time penalty for the first time and if repeated with disqualification. <br />
	-	Continuously disturbing other competitors may lead to disqualification. The race director holds the right to decide in such situation<br />
	-	There is a contingent for each category. If the contigent is filled for a category, priority belongs to registered (who completed the payment) participant.  <br />
	-	Cancellation and Refund policies are stated in the “Participation Fee” section.<br />
	-	Registrations close on June 17th, 2019. Switching between categories is not possible after registration closes. <br />
	-	All categories -EXCEPT 15K- must present their health report indicating that there is no obstacle to compete in the race. Kindly send your report’s copy to info@uzunteap.com and bring the original report to the event area. 
