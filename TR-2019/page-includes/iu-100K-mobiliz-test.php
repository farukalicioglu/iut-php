						<h4 class="align-left"><strong>YARIŞ KURALLARI</strong></h4>

						<p>
• Parkurun tamamının kategoriler için belirlenen zaman aralıklarında ve koşullarda kat edilmesi gerekmektedir. Bunun dışındaki durumlarda yarış bitirilmemiş sayılacaktır. 
<br />
• Koşuya katılabilmek için 18 yaşını tamamlamış olmak gerekir. 
<br />
• Rota üzerinde bulunan kontrol noktalarının herhangi birinden geçmeyen koşucu yarıştan çıkmış sayılacaktır. 
<br />
• Lojistik olarak her bir kontrol noktası önceden bildirilecek bir zaman sonunda kapatılacaktır. Bu süre içinde kontrol noktasına ulaşamayan koşucular yarıştan çıkmış sayılacaktır. Yarış direktörü tarafından zamanlamalarda yarış şartlarına göre esneklikler yapılabilir. 
<br />
• Katılımcılara belirlenen kontrol noktalarında gıda takviyesi atıştırmalık ve su temin edilecektir. Detaylar için “parkur” sayfasını ziyaret ediniz.
<br />
• IU-100K  (100 Km) yarışmacıları, ek yiyecek ve ekipmanlarını <strong>DROPBAG</strong> alanındaki yetkililere teslim edebilir ve bu noktaya ulaştıklarında teslim ettikleri çantalarda bulunan ekipmanlarını kullanabilirler. <strong>DROPBAG</strong> alanı, 47.3'üncü km'de bulunan "<strong>ROUND CP</strong>" olarak belirlenmiştir.<br>
Yarışmacılar, <strong>DROPBAG</strong> alanına teslim ettikleri çantaları yalnızca bir kez kullanabilir.
<br />
• Parkur içinde su belirli aralıklarla ve yetecek miktarda organizasyon tarafından sağlanacaktır. Her koşucu yeterince su almak ve içmek zorundadır. Dehidrasyon sebebiyle serum takılması gereken koşucu ilkinde 2 saat, ikincide 4 saat daha sonra yarıştan çıkarılma cezası alır. 
<br />
• Kategorilere göre farklılık göstermekle birlikte, katılımcılar her bir kategori için belirlenen taşınması zorunlu malzemelerini tüm koşu boyunca kendi sırt çantalarında taşıyacaktır. Zorunlu malzemelerin yarış esnasında bulundurulmaması yarıştan çıkarılmaya sebep olur. Organizasyon görevli ve hakemleri yarışın herhangi bir anında zorunlu malzeme ve çanta kontrolü yapma hakkına sahiptir. Zorunlu malzemeleri herhangi bir sebeple yanlarında bulundurmayan ya da yukarıda belirtilen kurallara uymayan sporculara, hakem kararıyla yarış dışı bırakılan sporculara ücret iadesi yapılmaz. 
<br />
• Yarış sırasında süresi ve mesafesi ne olursa olsun herhangi bir taşıyıcıya binme (motorlu araç, bisiklet, at arabası, at, eşek, katır vs.) yarıştan çıkarılmaya sebep olacaktır. 
<br />
• Ana etkinlik alanı ve kontrol noktalarında belirlenen alanlar dışında çöp atmak, ilkinde 2 saat süre daha sonra yarıştan çıkarılma ile cezalandırılır. 
<br />
• Diğer koşucuları ısrarlı olarak rahatsız edici şekildeki davranışlar yarıştan çıkarılmaya sebep olabilir. Bu konuda karar verme yetkisi yarış direktöründedir.
<br />
• Yarışma kayıt işlemi ilgili kategorinin katılım ücretinin ödenmesiyle gerçekleşmiş sayılır. Ödeme yapılmadan kesin kayıt gerçekleşmiş sayılmaz.
<br />
• İptal ve iade koşulları, “kayıt ve katılım ücretleri” sayfasında belirtilmiştir.
<br />
• Kayıtlar 17 Haziran 2019'da kapatılacaktır. Kayıtların kapanmasının ardından kategoriler arasında değişiklik / geçiş gerçekleştirilemez. 15 km üzeri kategorilerde yarışacak katılımcıların, yarışmalarına engel olmadığını belirten bir sağlık raporu sunmaları gerekmektedir. Bu sağlık belgesinin taranmış birer kopyası son kayıt tarihine kadar dijital olarak mail yoluyla organizasyona ulaştırılmalıdır. Belgelerin orijinalleri, kayıt ve kit teslimi sırasında organizasyon ekibine teslim edilmelidir. 
<br />
<a href="http://istanbulultratrail.com/files/istanbul-ultra-saglik-belgesi.docx" target="_blank">→ Sağlık raporunu indirmek için buraya tıklayınız.</a> 
<br />
• Yarış fiziksel ve ruhsal olarak üst düzeyde dayanıklılık gerektirdiğinden katılımcıların iyi bir hazırlık dönemi geçirmiş olması gereklidir. Her koşucu yarıştan önce koşuya kendi isteğiyle katıldığını ve sorumluluğun kendisinde olduğunu kabul eden bir muvaffakatname imzalayacaktır. 
<br />
• 15K kategorisi için klasifikasyon Kadınlar ve Erkekler olmak üzere sadece Genel Klasman’da yapılacaktır.
<br />
• 30K / 60K / 100K için klasifikasyon aşağıdaki gibi olacaktır:
<br />
&nbsp; &nbsp; * Kadınlar ve Erkekler Genel Klasman
<br />
&nbsp; &nbsp; * Kadın / Erkek 35 yaş altı (1985 – 2001 doğumlular)
<br />
&nbsp; &nbsp; * Kadın / Erkek 35 yaş ve üstü ( 1970 – 1984 doğumlular)
<br />
&nbsp; &nbsp; * Kadın / Erkek 50+ (Doğum tarihi 1969 ve öncesi doğumlular)
<br /><br />

• Her koşucunun finish süresi eğer varsa ceza sürelerinin eklenmesi ile belirlenecektir. İtiraz durumunda organizasyonun kayıtları esas alınacaktır. Ayrıca her bir kontrol noktasına varış süreleri de kaydedilecektir. 
<br />
• Dereceye girenlere koşu sonrası düzenlenecek törende kupa ve madalya verilecektir. Para ödülü yoktur.
<br />
• Yarış boyunca profesyonel arama kurtarma ve sağlık ekipleri bulunacaktır. Medikal hizmetler ana etkinlik alanında verilecek ancak parkur içinde ve kontrol noktalarında da basit müdahaleler yapılabilecektir. Medikal ekip yarış sırasında sahada da görev yapacaktır. Medikal ekip yarışçıların tıbbi durumları uygun değilse tek taraflı olarak koşucuyu yarıştan çıkarabilir. Ancak koşucunun yarışı bitirebilmesi organizasyonun da tercihidir. 
<br />
• Yarışın rotasının sorumluluğu ve yarış süresince yönetim yarış direktöründedir. Rota, koşuculara yarış öncesi yol haritası şeklinde verilecektir. Hava koşulları veya diğer etmenler sebebiyle yarış rotası değiştirilebilir, buna karar vermek yarış direktörünün yetkisindedir. Kontrol noktalarının yerleri yine yarış direktörü kararıyla değiştirilebilir. 
<br />
• Katılımcılar yanlarındaki değerli eşyaları ve diğer eşyaları yarıştan önce organizasyonca belirlenen görevliye teslim edebilirler. Yarış bitiminde bu eşyalar kendilerine teslim edilecektir. Yarış süresince sırt çantalarında taşınacak eşyaların sorumluluğu sporcunun kendisine aittir. 
<br />
• Yarışmacılar koşu için sponsor kullanabilirler. Sponsor logoları göğüs ve sırt çantası üzerine konulacak numaraları örtmeyecek şekilde istendiği gibi koşucunun üstünde bulunabilir. Bunun dışındaki alanlardaki sponsor logoları organizasyon komitesi tarafından belirlenir. 
<br />
• Yarışmacılar istedikleri noktada yarışı bırakabilirler. Yarışı bırakmış sayılmak için görevlilere haber verilmesi gerekmektedir.
<br />
• Kayıt yaptıran sporcular, yarış start saati öncesi yarışmacı kitlerini almakla yükümlüdür. Etkinlik bittikten sonra herhangi bir şekilde kit dağıtımı/gönderimi yapılmayacaktır.
<br />
• Yarış öncesinde yapılacak kontrol sonrası, zorunlu malzeme listesinde belirtilen malzemelere sahip olmayan sporcular yarışa kabul edilmeyecektir.
<br />
• Zorunlu malzemeleri bulundurmayan ya da yukarıda belirtilen kurallara uymayan ve herhangi bir sebeple, hakem kararıyla yarış dışı bırakılan sporculara ücret iadesi yapılmaz.
<br /><br />


<strong>Diskalifikasyon</strong><br />
Aşağıdaki maddelerden herhangi birinin gerçekleşmesi durumunda, ilgili yarışmacı doğrudan diskalifiye edilir. 
<br />
• Kontrol noktalarından geçmemek<br />
• Parkurda kontrol noktaları dışında çöp atmak<br />
• Belirlenen rota dışında kestirme yollar kullanmak<br />
• Organizasyon tarafından verilen göğüs numarası ve çipi, yine organizasyon tarafından belirtilen şekilde üzerinde bulundurmamak.<br />
• Sağlık personeli ve yarış direktörü tarafından kontrol edilmeyi reddetmek ve organizasyon tarafından alınan kararlara uymamak.<br />
• Zorunlu malzemelerinden herhangi birini bulundurmamak ve/veya zorunlu malzeme kontrolünü reddetmek.<br />
<br />
<strong>Yarışa kayıt yaptıran tüm koşucular yarış kurallarını tümüyle kabul etmiş sayılırlar.</strong></p>


