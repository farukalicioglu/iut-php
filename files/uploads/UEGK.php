                        <h4 class="align-left"><strong>Kurallar & Genel Bilgiler</strong></h4>
                        <p>Yarışa katılabilmek için 18 yaşını tamamlamış olmak gerekir. 18 yaşının altındaki katılımcılar eşlik eden (koşucu olarak) ebeveynlerinin varlığında yarışa katılabilirler. 18 yaşın altındaki katılımcıların, kayıt esnasında, linkte yer alan Veli İzin Formunu doldurarak, yasal velilerine imzalatmaları ve organizasyon yetkililerine fakslamaları gerekmektedir. Formun aslı yarış başlangıç esnasında organizasyon görevlilerine teslim edilmelidir.</p>
                        <p>Katılımın gerçekleşebilmesi için, katılımcının başvuru bölümünden gerekli formu doldurması ve katılmak istediği kategoriye ait katılım ücretini yatırmış olması gerekir. Katılımcıların yarış boyunca üzerlerinde taşımakla yükümlü oldukları göğüs numaraları, yarış günü start noktasında kurulacak kayıt masasından dağıtılacaktır.</p>
                        <p>Organizasyon ekibi ve hakemler yarışın herhangi bir noktasında sporcunun sağlığı ile ilgili riskli bir durum görmesi halinde koşucuyu yarış dışı bırakma hakkına sahiptir.</p>
                        <p>Yarış öncesinde yapılacak kontrol sonrası, zorunlu malzeme listesinde belirtilen malzemelere sahip olmayan sporcular yarışa kabul edilmeyecektir.</p>
                        <p>Zorunlu malzemeleri bulundurmayan ya da yukarıda belirtilen kurallara uymayan ve herhangi bir sebeple, hakem kararıyla yarış dışı bırakılan sporculara ücret iadesi yapılmaz.</p>
                        <p><strong>Kayıt & Başvuru</strong><br />
Kayıt işlemleri online olarak yapılır. Başvuru sekmesinde istenen bilgileri eksiksiz girerek ön kaydınızı oluşturabilir ve ödeme işlemlerini gerçekleştirerek kesin kaydınızı tamamlayabilirsiniz. Kaydınız tamamlandıktan sonra otomatik bir e-posta ile kayıt onayınız size ulaşacaktır. Otomatik onay maili ulaşmaması durumunda lütfen spam klasörünüzü kontrol ediniz ve organizasyon yetkililerini <a href="mailto:info@uzunetap.com">info@uzunetap.com</a> adresinden bilgilendiriniz. <br />Kesin kaydı tamamlanmış olan katılımcılar “Kayıtlı Koşucular” listesinden isimlerini kontrol edebilirler.<br />
Yarışmacılar, kitlerini Pazar günü yarış alanında kurulacak olan kit dağıtım alanından kimlik ibrazı ile alabilirler.</p>
                        <p><strong>Mesuliyet ve Sigorta</strong><br />
Organizasyona katılımda mesuliyet katılımcılara aittir. Organizatör firma her türlü mesuliyetten muaftır. Buna kazalar, kayıp eşyalar ya da başka araç / gereçler de dâhildir. Katılım belgelerinin ulaşmasıyla tüm katılımcılar, kendilerinin ve ekip arkadaşlarının koşuya katılmalarında herhangi bir sağlık engelinin bulunmadığını kabul ederler.<br />
Bununla birlikte TAF (Türkiye Atletizm Federasyonu) özel yol yarışları talimatı gereğince yarışmaya katılan tüm sporcular yarış süresi boyunca Federasyon tarafından sigortalanır. Sigorta bedelleri organizatör firma tarafından karşılanır.</p>
                        <p><strong>Diskalifikasyon</strong><br />
Göğüs numarasının ve çipinin başka kişilere devredilmesi, diskalifiye nedenidir.<br />
Yarış güzergâhından çıkılması veya güzergâhın değiştirilmesi yarışmacının diskalifiye edilmesine neden olur.</p>
                    
